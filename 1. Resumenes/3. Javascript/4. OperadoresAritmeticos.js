/* OPERADORES ARITMÉTICOS */

let x = 15;
let y = 4;

/* Suma */
let suma = x + y;

console.log(suma);

/* Resta */
let resta = x - y;

console.log(resta);

/* Multiplicación */
let multiplicacion = x * y;

console.log(multiplicacion);

/* División */
let division = x / y;

console.log(division);

/* Modulo */
let modulo = x % y;

console.log(modulo);

/* Incremento */
let incremento = ++x;

console.log(incremento);

/* Decremento */
let decremento = --x;

console.log(decremento);